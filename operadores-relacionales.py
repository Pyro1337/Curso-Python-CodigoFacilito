numero_uno = 10;
numero_dos = 20;

"""Estos operadores relacionales son:
> Mayor
< menor
<= menor o igual
>= Mayor o igual
== igual
!= distinto"""
print("Numero uno : ",numero_uno,"\nNumero dos : ",numero_dos);
resultado = numero_uno > numero_dos;
print("Numero uno es mayor que numero dos ?: ",resultado);
resultado = numero_uno < numero_dos;
print("Numero uno es menor que numero dos ?: ",resultado);
resultado = numero_uno <= numero_dos;
print("Numero uno es menor o igual a numero dos ?: ",resultado);
resultado = numero_uno >= numero_dos;
print("Numero uno es menor o igual a numero dos ?: ",resultado);
resultado = numero_uno == numero_dos;
print("Numero uno es igual a numero dos ? : ",resultado);
resultado = numero_uno != numero_dos;
print("Numero uno es distinto a numero dos ? : ",resultado);