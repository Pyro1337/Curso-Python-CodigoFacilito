#Entre los operadores logicos nos encontramos a and, or y se comparan valores booleanos para obtener valores booleanos.
#Para and todas las comparaciones deben ser verdaderas.
#Para or basta que con una sea verdadera
#Para not no debe ser, es decir es lo opuesto.
resultado_final = True and True;
print(resultado_final);
resultado_final = True and False;
print(resultado_final);
resultado_final = True or False;
print(resultado_final);
resultado_final = not True;
print(resultado_final);
resultado_final = not False;
print(resultado_final);